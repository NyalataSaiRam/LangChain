def plus(n1:int, n2:int):
    """adds two numbers
    Args: 
        n1: int
        n2:int
    return:
        str
    """
    return f"{n1} plus {n2} = {n1+n2}"

def divide(n1:int, n2:int):
    """divides two numbers
    Args: 
        n1: int
        n2:int
    return:
        str
    """
    return f"{n1} divide {n2} = {n1/n2}"